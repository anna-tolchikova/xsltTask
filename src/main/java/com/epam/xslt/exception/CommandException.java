package com.epam.xslt.exception;

public class CommandException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 7584313925854071561L;

	public CommandException() {
		super();
	}

	public CommandException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CommandException(String arg0) {
		super(arg0);
	}

	public CommandException(Throwable arg0) {
		super(arg0);
	}

	
	
}
